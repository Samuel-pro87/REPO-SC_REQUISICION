        <p>Conquista a esa persona especial que hace latir tu corazón con estas frases de amor las cuales te ayudaran a expresar todos tus sentimientos al chico o chica que te gusta.</p>
<p>No sabia lo que era la soledad porque nunca nadie me hizo falta, hasta que llegaste tu, entonces lo entendí tu me hacías mucha falta.</p>
<p>Quisiera poder abrazarte, quisiera poder tomar tu mano, quisiera poder besarte, quisiera tantas cosas pero me conformo con tu sonrisa.</p>
<p>No se si exista el destino yo solo se que quiero estés en ese destino así como estas en mis pensamientos.</p>
<p>La forma en la que sonríes es lo que me enamoro poco a poco en cada sonrisa.</p>
<p>Quisiera tenerte un poco mas cerca de mi para que supieras cuan feliz me hace decir tu nombre.</p>
<p>Se que sonríes cada vez que te envió frases de amor por me gusta enviártelas.</p>
<p>Tu sonrisa eso sin duda cambia mi día, mi mundo, mi existencia y la razón es porque se que es por mi.</p>
<p>Aunque sea solo un instante, un momento, quiero estar contigo, porque cuando estoy contigo puedo sonreír sin motivo y siento felicidad en mi alma.</p>
<p>Llegaste a mi como todas las personas pero te quedaste muy dentro de mi como nadie.</p>
<p>Con frases de amor me gustaría llegar poco a poco hasta tu corazón.</p>
<p>Llegaste muy dentro de mi sin que lo supiera, sin que lo sintiera, sin que lo deseara y ahí te quedaste, y es donde quiero que estés para siempre, muy dentro de mi.</p>
<p>Mis latidos se aceleran cuando estoy contigo y no es que te tenga miedo, es la emoción de poder estar contigo lo que impulsa cada latido.</p>
<p>No te conozco, pero siento que te llevo esperando una vida, no se tu nombre pero siempre estas en mis pensamientos.</p>
<p>Aunque no todo salga bien, quiero intentarlo, aunque no te merezca quiero estar contigo, aunque me duela quiero amarte.</p>
<h2><a href="https://nordanuy.com/wp-content/uploads/2019/01/frases-tiernas-de-amor-para-dedicar.jpg"><img class="alignnone size-medium wp-image-59" src="https://nordanuy.com/wp-content/uploads/2019/01/frases-tiernas-de-amor-para-dedicar-300x225.jpg" alt="frases de amor" width="300" height="225" srcset="https://nordanuy.com/wp-content/uploads/2019/01/frases-tiernas-de-amor-para-dedicar-300x225.jpg 300w, https://nordanuy.com/wp-content/uploads/2019/01/frases-tiernas-de-amor-para-dedicar-768x576.jpg 768w, https://nordanuy.com/wp-content/uploads/2019/01/frases-tiernas-de-amor-para-dedicar-80x60.jpg 80w, https://nordanuy.com/wp-content/uploads/2019/01/frases-tiernas-de-amor-para-dedicar.jpg 800w" sizes="(max-width: 300px) 100vw, 300px" title="frases de amor"></a></h2>
<p>Cada vez que te veo y me sonríes, me doy cuenta de cuánto me muero por besarte.</p>
<p>Después de que llegaste a mi lo supe, si le pido algo más a la vida, es demasiado, porque contigo ya he quedado en deuda.</p>
<p>Mis deseos de verte son más grandes que la distancia entre los dos.</p>
<p>Te quiero, no solo por ser como eres… también por cómo me haces sentir cuando estoy contigo.</p>
<p>Significas para mí, mucho más de lo que pueda expresar con palabras.</p>
<p>Hay muchas frases de amor pero siento que ninguna puede expresar cuanto es que me importas.</p>
<p>Al tenerte a mi lado, todo es más bonito y estas frases de amor son mi forma de decirte que contigo todo es mejor.</p>
<p>Quiero aprovechar cada instante a tu lado, que nuestro amor dure lo que tenga que durar: un segundo, días, meses, años… pero que estemos juntos.</p>
<p>A cada paso que doy, tú eres la luz que ilumina mi camino al amor.</p>
<p>Si me ves mañana muy feliz, eso es porque te soñé, amor mío.</p>
<p>Un amor como el nuestro no tiene por qué ser perfecto, sólo me hará feliz que sea sincero.</p>
<p>No tiene idea del desorden de emociones que tu hermosa sonrisa provoca en mí.</p>
<h2><a href="https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-dedicar.jpg"><img class="alignnone size-medium wp-image-57" src="https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-dedicar-300x225.jpg" alt="frases de amor" width="300" height="225" srcset="https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-dedicar-300x225.jpg 300w, https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-dedicar-768x576.jpg 768w, https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-dedicar-80x60.jpg 80w, https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-dedicar.jpg 800w" sizes="(max-width: 300px) 100vw, 300px" title="frases de amor"></a></h2>
<p>Guardare siempre ese lindo recuerdo, esa linda sonrisa y esa dulce mirada.</p>
<p>Aunque en esta vida no pueda estar junto a ti, prometo que te buscare en la siguiente, solo espérame.</p>
<p>Mis pensamientos no entiende porque si tu y yo nos amamos no podemos estar juntos.</p>
<p>No importa el tiempo que pase o la distancia que nos separa yo siempre te tendré en dentro de mi esperando el momento en el que podamos estar juntos.</p>
<p>Aunque hoy no podamos estar juntos se que en algún momento podre estar contigo otra vez y buscare ese momento.</p>
<p>No entiendo al destino, hizo que nos encontráramos, hizo que nos enamoráramos pero porque no deja que estemos juntos.</p>
<p>Si hace falta ignorare al destino y empezare a escribir un destino, uno en el que podamos estar juntos.</p>
<p>Tal vez no podamos estar juntos mas que en sueños y en nuestros pensamientos.</p>
<p>No se porque te conocí, no se porque me enamore de ti, no se porque si te amo tanto no puedo estar contigo, lo único que se es que jamas te olvidare.</p>
<p>Dos almas que no pueden estar juntas siempre se buscaran intentando encontrarse la una a la otra.</p>
<p>Tal vez hoy no podamos estar juntos pero si es necesario buscare un mañana en el si podamos estar juntos.</p>
<p>Te extraño como cuando hace mucho frió, deseando un poco de tu calor, porque se que solo tu derretirás mi helado corazón.</p>
<p>Si sientes en algún momento que alguien en algún lugar te extraña, ese soy yo extrañándote y esperándote.</p>
<p>No se como ni donde sucedió pero cuando me di cuenta ya te extrañaba como a nadie.</p>
<p>Sabes que amas a alguien cuando extrañas a esa persona como si solo esa persona fuera tu razón de existir.</p>
<p>Se que prometí que no te extrañaría pero es difícil no extrañarte si estas siempre en mi mente.</p>
<p>No hay día en el que no estés en mi mente ni noche que no te sueñe y es por que te extraño.</p>
<p>Sabes que extrañas a alguien cuando sin querer dices su nombre con una sonrisa.</p>
<p>Si en tus sueños yo estoy presente abrázame y besame como si no existiera un mañana porque te extraño.</p>
<p>Extraño tu voz, tu sonrisa, tus ojos, tu perfume, tus abrazos y yo creo simplemente extraño todo de ti.</p>
<p>No se porque y no se que seamos tu y yo pero te extraño.</p>
<p>Yo creo que mi mayor vicio eres tu ya que no dejo de extrañarte ni un solo instante.</p>
<p>Hemos vivido muchas cosas, tenemos sonrisas, abrazos, besos, momentos y por eso te extraño porque quiero un poco mas.</p>
<h2><a href="https://nordanuy.com/wp-content/uploads/2019/01/frases-lindas-sobre-el-amor.jpg"><img class="alignnone size-medium wp-image-58" src="https://nordanuy.com/wp-content/uploads/2019/01/frases-lindas-sobre-el-amor-300x225.jpg" alt="frases de amor" width="300" height="225" srcset="https://nordanuy.com/wp-content/uploads/2019/01/frases-lindas-sobre-el-amor-300x225.jpg 300w, https://nordanuy.com/wp-content/uploads/2019/01/frases-lindas-sobre-el-amor-768x576.jpg 768w, https://nordanuy.com/wp-content/uploads/2019/01/frases-lindas-sobre-el-amor-80x60.jpg 80w, https://nordanuy.com/wp-content/uploads/2019/01/frases-lindas-sobre-el-amor.jpg 800w" sizes="(max-width: 300px) 100vw, 300px" title="frases de amor"></a></h2>
<p>Estoy ansioso por comenzar a escribir nuestra historia de amor.</p>
<p>Te juro que al abrazarme, sentiré como el mundo se detiene para disfrutar ese breve instante.</p>
<p>El secreto para sentirme feliz es hacerte feliz a ti.</p>
<p>Desde que te conocí, has cambiado de rumbo, camino a la felicidad y mi forma de ver el mundo</p>
<p>Al conocerte a ti, conocí la verdadera paz en mi alma, porque a tu lado encontré el amor.</p>
<p>Es un privilegio ser parte de tu vida, y que tu estés siempre en mis pensamientos.</p>
<p>Cada instante a tu lado me regala días de felicidad.</p>
<p>Quizás el sol ilumina los días, y la luna las noches, pero tú, amada mía, iluminas cada instante de mi existencia.</p>
<p>Esto ha sido maravilloso, y es que nos encontramos sin siquiera estar buscándonos.</p>
<p>Estarás lejos de mi vista… Pero siempre estarás muy dentro de mi, grabada en mi alma.</p>
<h2><a href="https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-compartir.jpg"><img class="alignnone size-medium wp-image-56" src="https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-compartir-300x225.jpg" alt="frases de amor" width="300" height="225" srcset="https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-compartir-300x225.jpg 300w, https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-compartir-768x576.jpg 768w, https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-compartir-80x60.jpg 80w, https://nordanuy.com/wp-content/uploads/2019/01/frases-de-amor-para-compartir.jpg 800w" sizes="(max-width: 300px) 100vw, 300px" title="frases de amor"></a></h2>
<p>Yo pensaba que tenía todo, hasta que llegó el día en que te conocí, y me di cuenta que estaba vacío sin ti.</p>
<p>No se trata de encontrar el amor perfecto, se trata de encontrar a alguien que con sus virtudes y defectos, hace que la vida sea perfecta.</p>
<p>Todo es diferente los días como hoy simplemente ahora es perfecto, y el secreto es que tú estás conmigo.</p>
<p>No importa el destino, sólo me importa que esté contigo.</p>
<p>Desde que te conocí has sido, eres y seguirás siendo mi más hermosa casualidad.</p>
<p>El amor que te tengo, no se expresa con palabras, se expresa con mis abrazos, mis sonrisas y mis besos.</p>
<p>Siempre pude brillar sólo, pero desde que estoy a tu lado, no brillo… en realidad soy resplandeciente.</p>
<p>Qué suerte tengo, sin buscarlo, encontré la felicidad, y ahora conozco al fin que es el amor, y todo gracias a ti. Te amo, mi princesa.</p>
<p>No tengo idea de donde estará mi lugar, lo que si tengo la certeza de que es a tu lado, mi amor.</p>
<p>Has sido, eres y siempre serás la mejor casualidad que ha llego a mi, para hacerme muy feliz.</p>
<p>Amor, nunca encontrarás el amor de tu vida, porque éste se construye, así como lo estamos haciendo entre los dos.</p>
<p>Tu perfume es delicioso, pero siento que el sabor de tus besos no tiene comparación.</p>
<p>Definitivamente, conocerte no ha sido algo bueno en mi vida… Ha sido lo mejor de mi vida, te amo, mi vida.</p>
<p>Quisiera explicarte que eres el amor de mi vida, y definitivamente eres la causa por la que no duermo temprano.</p>
<p>No puedo precisar el momento en el que esto me ha ocurrido, tampoco sé el motivo, pero me enamoré de ti, mi amor.</p>
<p>Siempre te amaré, sin importar lo que diga, ni lo que haga, o lo que sufra, esté cerca o esté lejos, siempre te amaré.</p>
<p>Una vez escuché que si te dan besos hasta en la punta de la nariz, eso es amor… Lo averiguaré.</p>
<p>Un amor verdadero es aquel que el viento nunca eleva, y la distancia nunca separa. Así es nuestro amor.</p>
<p>Aunque siempre me han dicho que nadie es imprescindible, no puedo negar que mi vida sería triste y vacía sin ti.</p>
<p>Quiero serte siempre honesto, te prometo que en ocasiones discutiremos, a veces nos molestaremos, pero siempre serás el amor de mi vida, y todo lo superaremos.</p>
<p>Cada vez que sueño contigo, me cuesta tanto volver a despertar.</p>
<p>Un suspiro dura si acaso un segundo de tu vida, pero un amor puro y sincero como el que te ofrezco, durará toda la vida.</p>
<p>Definitivamente, tu llegada a mi vida es la mejor parte de mi historia que el destino ha escrito.</p>
<p>He vivido plenamente, si debo arrepentirme de algo, definitivamente es de no haberte conocido antes.</p>
<p>Pensaba que eras perfecto, y me enamoré de ti… Luego me di cuenta, que no eres tan perfecto, a partir de ese momento te amé más que nunca.</p>
<p>Existe algo que debo agradecerle a la vida, y es que me permitió la dicha y el honor de coincidir contigo, y vivir esta experiencia a tu lado.</p>
<p>No sé si vas a leer este mensaje hoy, o lo leerás mañana, lo cierto es que te amo, y nada cambiará ese sentimiento.</p>
<p>Seguiré el camino que sea necesario para llegar a tu lado, porque mi mayor deseo es estar contigo.</p>
<p>No me interesa si los demás piensen que no eres perfecto, pero para mí eres alguien lleno de virtudes y defectos, pero me haces feliz, y por eso eres perfecto para mí.</p>
<p>Evita quedarte con esas hermosas expresiones de amor para esa persona especial en tu vida, dedícale alguna de estas frases, y demuéstrale lo especial que es para ti.</p>
